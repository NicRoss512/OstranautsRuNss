def main():
    with open(SRC, 'r', encoding='utf-8') as f:
        text = f.read()

    # Parse JSON-ish (it has comments). Strip leading // comments and use json.
    text_no_comments = re.sub(r'//[^\n]*', '', text)
    data = json.loads(text_no_comments)

    tokens2 = data[0]['tokens2']
    out_tokens = []
    not_found = []
    for entry in tokens2:
        # entry: ["singular"] or ["singular", "base"]
        en_sg = entry[0]
        if en_sg == "":
            out_tokens.append(["", ""])
            continue
        if en_sg not in T:
            not_found.append(en_sg)
            # fallback: just transliterate the english to ru placeholder
            out_tokens.append([en_sg, en_sg])
            continue
        ru_sg, ru_base = T[en_sg]
        if len(entry) >= 2 and entry[1]:
            # preserve 2-element form
            out_tokens.append([ru_sg, ru_base])
        else:
            # original had only 1 element; mimic
            out_tokens.append([ru_sg])

    data[0]['tokens2'] = out_tokens

    # Write preserving original comment header
    header = (
        "// format: [ singular verb, base verb ], becomes a dictionary keyed as singular verb : [singular verb, base verb]\n"
        "// when parsing strDescs, strings enclosed (in singular form) in square brackets will be compared with this dictionary to see if they match with known verbs, and can be conjugated in logs\n"
        "// to agree with a second person or third person voice, ie 'he is coding' vs 'you are coding'\n"
        "// in the case of duplicate keys, the latest keys will overwrite\n"
        "// update: given almost all verbs conjugate simply with an 's', I'm going to experiment making that the default behaviour and only specifying irregulars\n"
    )

    with open(DST, 'w', encoding='utf-8') as f:
        f.write(header)
        f.write('[\n')
        f.write('\t{\n')
        f.write('\t\t"strName" : "defaultVerbs",\n')
        f.write('\t\t"type" : "verbs",\n')
        f.write('\t\t"tokens2" : \n')
        f.write('\t\t[\n')
        for i, item in enumerate(out_tokens):
            inner = ', '.join(json.dumps(x, ensure_ascii=False) for x in item)
            f.write('\t\t\t[' + inner + ']')
            if i < len(out_tokens) - 1:
                f.write(',')
            f.write('\n')
        f.write('\t\t]\n')
        f.write('\t}\n')
        f.write(']\n')

    print(f'Total entries: {len(out_tokens)}')
    print(f'Not found: {len(not_found)}')
    if not_found:
        for x in not_found:
            print(f'  MISS: {x}')
    print(f'Wrote: {DST}')
    print(f'Size: {os.path.getsize(DST)} bytes')


if __name__ == '__main__':
    main()
